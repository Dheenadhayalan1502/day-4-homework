class Rectangle
{
	public static void main(String[]args)
	{
		float length = 10.5f;
		float breadth = 20.5f;
		float area = length*breadth;
		float perimeter = 2*(length+breadth);
		
		System.out.println("area of rectangle = " + area);
		System.out.println("perimeter of rectangle = " + perimeter);
	}
}

		